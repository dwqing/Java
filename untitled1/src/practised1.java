public class practised1 {
    public static void main(String[] args) {
        final double PI =3.14;
        int diameter =10;
        int height =3;
        double volume =diameter*diameter/4*height*PI;
        System.out.println("改粮仓的体积="+volume+"立方米");
        int weight =750;
        System.out.println("该粮仓一共可储存"+weight*volume+"千克粮食");

    }
}
